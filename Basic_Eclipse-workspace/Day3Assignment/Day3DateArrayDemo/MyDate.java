package Day3DateArrayDemo;

public class MyDate {
	
	private int day;
	private int month;
	private int year;
	
	public MyDate(int day, int month, int year) {
		this.day = day;
		this.month = month;
		this.year = year;
	}
	
	public void display() {
		System.out.println("my date is :" + day + "/" + month + "/" + year + "\n");
	}
}
